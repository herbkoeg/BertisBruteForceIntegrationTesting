package de.hk.bfit.process;

import de.hk.bfit.io.FileAdapter;
import de.hk.bfit.io.GenericXmlHandler;
import java.io.IOException;
import java.sql.Connection;
import java.sql.ResultSet;
import java.sql.ResultSetMetaData;
import java.sql.SQLException;
import java.sql.Statement;
import java.util.ArrayList;
import java.util.Iterator;
import java.util.List;
import java.util.Map;
import javax.xml.bind.JAXBException;
import org.apache.commons.lang3.StringUtils;
import org.apache.log4j.Logger;
import org.junit.Assert;

public class TestCaseProcessor {

    private final Logger logger = Logger.getLogger(TestCaseProcessor.class);
    private final TestCase testCase;
    private final Map<String, String> variables;
    private final Connection connection;

    public TestCaseProcessor(TestCase testCase, Connection connection, Map<String, String> variables) {
        this.variables = variables;
        this.testCase = testCase;
        this.connection = connection;
    }

     public TestCaseProcessor(Connection connection) {
        this.variables = null;
        this.testCase = null;
        this.connection = connection;
    }

    public void generateExampleTestCase(String filename, String description, String sql) throws SQLException, ClassNotFoundException, IllegalArgumentException, JAXBException, IOException {
        List<String> resultList = processReferenceAction(sql);

        TestCase newTestCase = new TestCase(description, new ReferenceAction("Autogenerated Example", sql, resultList));

        GenericXmlHandler genericXmlHandler = new GenericXmlHandler();
        String content = genericXmlHandler.convertObjectToXML(newTestCase);
        FileAdapter.writeFile(filename, content);
    }

    public void assertReferences() throws IOException, JAXBException, SQLException, ClassNotFoundException {
        Iterator<ReferenceAction> itRef = testCase.getReferenceActions().iterator();

        while (itRef.hasNext()) {
            ReferenceAction reference = itRef.next();
            logger.info("executing '" + reference.getSelect() + "'");
            List<String> refResults = reference.getResults();
            if (!reference.getSelect().contains("order by")) {
                logger.info("Warning: SQL doesn't contain 'order by'");
            }
            List<String> actualResults = processReferenceAction(reference.getSelect());
            logger.info(actualResults.size() + " results ");

            Assert.assertEquals("Result size differs", refResults.size(), actualResults.size());

            int length = refResults.size();

            for (int i = 0; i < length; i++) {
                Assert.assertEquals(setVariables(refResults.get(i)), actualResults.get(i));
            }
        }
        logger.info("Success!");
    }

    public void processResetAction() throws ClassNotFoundException, SQLException {
        processCommandList(testCase.getResetAction().getSqlCommands());
    }

    public void processInitAction() throws ClassNotFoundException, SQLException {
        processCommandList(testCase.getInitAction().getSqlCommands());
    }

    private List<String> processReferenceAction(String sql) throws SQLException, ClassNotFoundException {
        sql = setVariables(sql);
        ResultSet rs = createResultset(sql);
        return createResultList(rs);
    }

    private String setVariables(String sql) {
        if (variables != null) {
            Iterator<String> it = variables.keySet().iterator();
            String[] searchList = new String[variables.size()];
            String[] replacementList = new String[variables.size()];
            int counter = 0;
            while (it.hasNext()) {
                String variable = it.next();
                searchList[counter] = "$" + variable;
                replacementList[counter] = variables.get(variable);
            }

            return StringUtils.replaceEachRepeatedly(sql, searchList, replacementList);
        }
        return sql;
    }

    private List<String> createResultList(ResultSet rs) throws SQLException {
        List<String> results = new ArrayList<>();
        ResultSetMetaData rsmd = rs.getMetaData();
        int columnCount = rsmd.getColumnCount();
        while (rs.next()) {
            StringBuilder sb = new StringBuilder();
            for (int i = 1; i <= columnCount; i++) {
                if (rs.getString(i) != null) {
                    sb.append(rs.getString(i).trim()).append(";");
                } else {
                    sb.append("null;");
                }
            }
            results.add(sb.toString());
        }
        return results;
    }

    private ResultSet createResultset(String sql) throws SQLException {
        Statement statement = connection.createStatement();
        return statement.executeQuery(sql);
    }

    private void processCommandList(List<String> sqlList) throws ClassNotFoundException, SQLException {
        connection.setAutoCommit(false);
        Statement statement = connection.createStatement();
        Iterator<String> it = sqlList.iterator();

        try {
            while (it.hasNext()) {
                String sql = it.next();
                sql = setVariables(sql);
                if (sql.toLowerCase().startsWith("update ") || sql.toLowerCase().startsWith("insert ") || sql.toLowerCase().startsWith("delete ")) {
                    logger.info("reset: execute " + sql);
                    statement.execute(sql);
                } else {
                    logger.info("reset: ignoring " + sql);
                }
            }
            connection.commit();
        } catch (SQLException e) {
            connection.rollback();
            throw e;
        } finally {
            connection.close();
        }
    }

}
